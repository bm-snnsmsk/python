print("range(10)".center(50,"*"))
for i in range(10) :
    print(i)

print("range(3,10)".center(50,"*"))
for i in range(3,10) :
    print(i)


print("range(3,10,2)".center(50,"*"))
for i in range(3,10,2) :
    print(i)

print("list(range(5,100,10))".center(50,"*"))
print(list(range(5,100,10)))





